#include "strs_helpers.h"

void	ft_strsprint(char **strs)
{
	int	i;

	i = 0;
	while (strs[i] != 0)
	{
		ft_printf("[%s]", strs[i]);
		i++;
	}
	ft_printf("\n");
}
